pessoa=input("Digite seu estado civil C para Casado, S para Solteiro, D para Divorciado, V para Viúvo, A para Amigado ")
if pessoa == "C":
    print("Casado")
elif pessoa == "S":
    print("Solteiro")
elif pessoa == "D":
    print("Divorciado")
elif pessoa == "V":
    print("Viúvo")
else:
    print("Amigado")
    